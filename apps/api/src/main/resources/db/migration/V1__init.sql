-- ============================================================================
-- synub-billing 통합 스키마 (단일 베이스라인). 스키마 = billing.
-- 카탈로그·구독·정기결제·조직/역할/인증·과금·운영(스케줄러/멱등키/사용량).
-- 모든 테이블·주요 컬럼에 COMMENT 로 자기설명. 하단에 데모 시드.
-- ============================================================================

-- ── 카탈로그 ────────────────────────────────────────────────────────────────
CREATE TABLE product (
    id           BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    company_id   BIGINT       NOT NULL,
    service_code VARCHAR(50)  NOT NULL,
    name         VARCHAR(100) NOT NULL,
    category     VARCHAR(50),
    description  TEXT,
    domain_url   VARCHAR(255),
    demo_url     VARCHAR(255),
    webhook_url  VARCHAR(255),
    status       VARCHAR(20)  NOT NULL DEFAULT 'active',
    sort_order   INT          NOT NULL DEFAULT 0,
    org_only     BOOLEAN      NOT NULL DEFAULT false,
    created_at   TIMESTAMPTZ  NOT NULL DEFAULT now(),
    UNIQUE (company_id, service_code)
);
COMMENT ON TABLE  product              IS 'SaaS 제품 카탈로그(문서분석·회계 등). 테넌트(company_id)별.';
COMMENT ON COLUMN product.service_code IS '제품 식별 코드(제품 연동 계약 키). 예: doc-analysis';
COMMENT ON COLUMN product.webhook_url  IS '구독 상태 변경 시 빌링이 통보할 제품 엔드포인트';
COMMENT ON COLUMN product.status       IS '노출 상태: active | hidden';
COMMENT ON COLUMN product.org_only     IS '조직 전용 제품(예: 그룹웨어) — true면 회사 컨텍스트에서만 구독 가능';

CREATE TABLE plan (
    id            BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    product_id    BIGINT       NOT NULL REFERENCES product(id),
    plan_code     VARCHAR(50)  NOT NULL,
    name          VARCHAR(100) NOT NULL,
    tagline       VARCHAR(255),
    amount        INT          NOT NULL,
    currency      VARCHAR(3)   NOT NULL DEFAULT 'KRW',
    billing_cycle VARCHAR(10)  NOT NULL,
    features      JSONB,
    is_highlight  BOOLEAN      NOT NULL DEFAULT false,
    status        VARCHAR(20)  NOT NULL DEFAULT 'active',
    sort_order    INT          NOT NULL DEFAULT 0,
    pricing_type  VARCHAR(10)  NOT NULL DEFAULT 'flat',
    created_at    TIMESTAMPTZ  NOT NULL DEFAULT now(),
    UNIQUE (product_id, plan_code)
);
COMMENT ON TABLE  plan               IS '제품 요금제(플랜). 청구 금액·주기의 원천.';
COMMENT ON COLUMN plan.amount        IS '1주기 청구액(원). per_seat이면 1좌석 단가.';
COMMENT ON COLUMN plan.billing_cycle IS '청구 주기: monthly | yearly';
COMMENT ON COLUMN plan.features      IS '플랜 기능 목록(JSON 배열) — entitlements features로 노출';
COMMENT ON COLUMN plan.pricing_type  IS '과금 방식: flat(정액) | per_seat(인원당)';

-- ── 고객·결제수단 ──────────────────────────────────────────────────────────
CREATE TABLE customer (
    id          BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    company_id  BIGINT      NOT NULL,
    external_id VARCHAR(64) NOT NULL,
    email       VARCHAR(255),
    phone       VARCHAR(20),
    created_at  TIMESTAMPTZ NOT NULL DEFAULT now(),
    UNIQUE (company_id, external_id)
);
COMMENT ON TABLE  customer             IS '결제 고객 — SSO 통합계정(external_id)에 매핑(JIT 프로비저닝).';
COMMENT ON COLUMN customer.external_id IS 'SSO 계정 external_id(토큰 sub)';
COMMENT ON COLUMN customer.phone       IS 'PG 청구용 전화번호(카드 등록 시 수집)';

CREATE TABLE billing_key (
    id             BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    customer_id    BIGINT       NOT NULL REFERENCES customer(id),
    pg_billing_key VARCHAR(255) NOT NULL,
    card_company   VARCHAR(50),
    card_last4     VARCHAR(4),
    card_type      VARCHAR(20),
    is_primary     BOOLEAN      NOT NULL DEFAULT false,
    status         VARCHAR(20)  NOT NULL DEFAULT 'active',
    owner_type     VARCHAR(20)  NOT NULL,
    owner_id       BIGINT       NOT NULL,
    created_at     TIMESTAMPTZ  NOT NULL DEFAULT now()
);
COMMENT ON TABLE  billing_key                IS 'PG 빌링키(정기결제용 카드). PG가 발급, 원문 카드번호 미저장.';
COMMENT ON COLUMN billing_key.pg_billing_key IS 'PG(포트원) 빌링키 토큰';
COMMENT ON COLUMN billing_key.owner_type     IS '소유 스코프: customer(개인) | organization(회사)';
COMMENT ON COLUMN billing_key.owner_id       IS '소유자 id(개인=customer.id, 회사=organization.id)';
COMMENT ON COLUMN billing_key.status         IS 'active | deleted';
CREATE INDEX idx_billing_key_customer ON billing_key (customer_id);
CREATE INDEX idx_billing_key_owner    ON billing_key (owner_type, owner_id);

-- ── 조직·역할·초대 ─────────────────────────────────────────────────────────
CREATE TABLE organization (
    id            BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    company_id    BIGINT       NOT NULL,
    name          VARCHAR(100) NOT NULL,
    business_no   VARCHAR(20),
    business_doc  VARCHAR(255),
    rep_name      VARCHAR(50),
    open_date     VARCHAR(8),
    rep_verified  BOOLEAN      NOT NULL DEFAULT false,
    verify_status VARCHAR(20)  NOT NULL DEFAULT 'pending',
    reject_reason VARCHAR(255),
    verified_at   TIMESTAMPTZ,
    created_at    TIMESTAMPTZ  NOT NULL DEFAULT now()
);
COMMENT ON TABLE  organization               IS '회사(조직) — 결제·구독의 소유 주체. 사업자 인증 후 이용.';
COMMENT ON COLUMN organization.business_no   IS '사업자등록번호(숫자 10자리, 국세청 진위확인 대상)';
COMMENT ON COLUMN organization.business_doc  IS '사업자등록증 파일 키(StorageService)';
COMMENT ON COLUMN organization.rep_name      IS '대표자명(국세청 진위확인·본인인증 대조)';
COMMENT ON COLUMN organization.open_date     IS '개업일자 YYYYMMDD(국세청 진위확인)';
COMMENT ON COLUMN organization.rep_verified  IS '대표자 본인인증 통과 기록(관리자 심사 참고)';
COMMENT ON COLUMN organization.verify_status IS '인증 상태: pending(심사대기) | verified(승인) | rejected(반려)';
COMMENT ON COLUMN organization.reject_reason IS '반려 사유(rejected일 때)';
-- 같은 사업자번호로 중복 회사 생성 방지
CREATE UNIQUE INDEX uq_org_business_no ON organization (business_no) WHERE business_no IS NOT NULL;

CREATE TABLE membership (
    id              BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    organization_id BIGINT      NOT NULL REFERENCES organization(id),
    customer_id     BIGINT      NOT NULL REFERENCES customer(id),
    role            VARCHAR(20) NOT NULL,
    created_at      TIMESTAMPTZ NOT NULL DEFAULT now(),
    UNIQUE (organization_id, customer_id)
);
COMMENT ON TABLE  membership      IS '조직 멤버십 — 고객이 어떤 회사에 어떤 역할로 속하는지.';
COMMENT ON COLUMN membership.role IS '역할: owner | billing_manager | member (owner/billing_manager만 결제 관리)';
CREATE INDEX idx_membership_customer ON membership (customer_id);

CREATE TABLE invitation (
    id                     BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    organization_id        BIGINT       NOT NULL REFERENCES organization(id),
    email                  VARCHAR(255) NOT NULL,
    role                   VARCHAR(20)  NOT NULL,
    status                 VARCHAR(20)  NOT NULL DEFAULT 'pending',
    invited_by_customer_id BIGINT       REFERENCES customer(id),
    expires_at             TIMESTAMPTZ,
    created_at             TIMESTAMPTZ  NOT NULL DEFAULT now()
);
COMMENT ON TABLE  invitation            IS '조직 멤버 초대 — 이메일로 pending 생성 후 대상이 수락하면 멤버십 전환.';
COMMENT ON COLUMN invitation.status     IS 'pending | accepted | declined | canceled';
COMMENT ON COLUMN invitation.expires_at IS '초대 만료 시각(기본 7일). 지나면 수락 불가.';
CREATE INDEX idx_invitation_org   ON invitation (organization_id);
CREATE INDEX idx_invitation_email ON invitation (email);
-- 같은 조직·이메일로 중복 pending 초대 방지
CREATE UNIQUE INDEX uq_invitation_pending ON invitation (organization_id, email) WHERE status = 'pending';

-- ── 구독·결제 ──────────────────────────────────────────────────────────────
CREATE TABLE subscription (
    id                   BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    customer_id          BIGINT      NOT NULL REFERENCES customer(id),
    plan_id              BIGINT      NOT NULL REFERENCES plan(id),
    billing_key_id       BIGINT      NOT NULL REFERENCES billing_key(id),
    status               VARCHAR(20) NOT NULL,
    started_at           TIMESTAMPTZ NOT NULL,
    next_billing_date    DATE        NOT NULL,
    cancel_at_period_end BOOLEAN     NOT NULL DEFAULT true,
    canceled_at          TIMESTAMPTZ,
    retry_count          INT         NOT NULL DEFAULT 0,
    seats                INT         NOT NULL DEFAULT 1,
    credit_balance       INT         NOT NULL DEFAULT 0,
    owner_type           VARCHAR(20) NOT NULL,
    owner_id             BIGINT      NOT NULL,
    created_at           TIMESTAMPTZ NOT NULL DEFAULT now()
);
COMMENT ON TABLE  subscription                      IS '구독 — 상태머신(active⇄past_due→suspended, canceled).';
COMMENT ON COLUMN subscription.status               IS 'active | past_due(결제실패 유예) | suspended(정지) | canceled';
COMMENT ON COLUMN subscription.next_billing_date    IS '다음 청구일(스케줄러가 이 날짜로 청구)';
COMMENT ON COLUMN subscription.cancel_at_period_end IS 'true면 현재 주기 종료 시 해지 예정';
COMMENT ON COLUMN subscription.retry_count          IS '결제 실패 재시도 횟수(재시도 정책 소진 시 suspended)';
COMMENT ON COLUMN subscription.seats                IS 'per_seat 과금 좌석 수';
COMMENT ON COLUMN subscription.credit_balance       IS '비례정산 크레딧 잔액(원) — 다음 청구에서 차감';
COMMENT ON COLUMN subscription.owner_type           IS '소유 스코프: customer | organization';
CREATE INDEX idx_subscription_customer ON subscription (customer_id);
CREATE INDEX idx_subscription_owner    ON subscription (owner_type, owner_id);
CREATE INDEX idx_subscription_due      ON subscription (next_billing_date, status);

CREATE TABLE payment (
    id              BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    subscription_id BIGINT       NOT NULL REFERENCES subscription(id),
    pg_payment_id   VARCHAR(255),
    amount          INT          NOT NULL,
    status          VARCHAR(20)  NOT NULL,
    failure_reason  TEXT,
    receipt_no      VARCHAR(64),
    paid_at         TIMESTAMPTZ,
    created_at      TIMESTAMPTZ  NOT NULL DEFAULT now()
);
COMMENT ON TABLE  payment               IS '결제 내역(청구 시도 결과). 환불·실패 포함.';
COMMENT ON COLUMN payment.pg_payment_id IS 'PG 결제건 ID(멱등키 겸 웹훅 대조 키)';
COMMENT ON COLUMN payment.status        IS 'paid | failed | refunded | pending';
COMMENT ON COLUMN payment.paid_at       IS '결제 완료 시각(실패면 NULL)';

-- ── 사용량(제품 보고) ──────────────────────────────────────────────────────
CREATE TABLE usage_record (
    id           BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    external_id  VARCHAR(120) NOT NULL,
    service_code VARCHAR(60)  NOT NULL,
    label        VARCHAR(60)  NOT NULL,
    unit         VARCHAR(20)  NOT NULL,
    used         INT          NOT NULL DEFAULT 0,
    limit_qty    INT,
    updated_at   TIMESTAMPTZ  NOT NULL DEFAULT now(),
    UNIQUE (external_id, service_code)
);
COMMENT ON TABLE  usage_record           IS '제품이 보고한 이번 주기 사용량(대시보드 표시). (external_id, service_code) upsert.';
COMMENT ON COLUMN usage_record.used      IS '사용량';
COMMENT ON COLUMN usage_record.limit_qty IS '한도(무제한이면 NULL)';

-- ── 운영(웹훅 발송·멱등키·분산락) ──────────────────────────────────────────
CREATE TABLE webhook_delivery (
    id            BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    product_id    BIGINT      REFERENCES product(id),
    event         VARCHAR(50) NOT NULL,
    url           VARCHAR(255),
    payload       TEXT,
    status        VARCHAR(20) NOT NULL DEFAULT 'pending',
    attempts      INT         NOT NULL DEFAULT 0,
    last_error    TEXT,
    response_code INT,
    delivered_at  TIMESTAMPTZ,
    created_at    TIMESTAMPTZ NOT NULL DEFAULT now()
);
COMMENT ON TABLE  webhook_delivery        IS '제품 웹훅 발송 감사 로그(재시도 포함).';
COMMENT ON COLUMN webhook_delivery.event  IS 'subscription.activated / payment_failed / suspended / canceled / plan_changed';
COMMENT ON COLUMN webhook_delivery.status IS 'pending | delivered | failed';
CREATE INDEX idx_webhook_delivery_event  ON webhook_delivery (event);
CREATE INDEX idx_webhook_delivery_status ON webhook_delivery (status);

CREATE TABLE idempotency_key (
    id            BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    scope         VARCHAR(64)  NOT NULL,
    idem_key      VARCHAR(128) NOT NULL,
    customer      VARCHAR(64)  NOT NULL,
    status        VARCHAR(20)  NOT NULL,
    response_body TEXT,
    created_at    TIMESTAMPTZ  NOT NULL DEFAULT now(),
    UNIQUE (scope, idem_key)
);
COMMENT ON TABLE  idempotency_key               IS '멱등키 — 동일 요청 재실행 방지·저장 응답 재생.';
COMMENT ON COLUMN idempotency_key.scope         IS '멱등 범위(예: create-subscription)';
COMMENT ON COLUMN idempotency_key.status        IS 'in_progress | done';
COMMENT ON COLUMN idempotency_key.response_body IS '완료된 응답 본문(재요청 시 그대로 반환)';

CREATE TABLE shedlock (
    name       VARCHAR(64)  PRIMARY KEY,
    lock_until TIMESTAMPTZ  NOT NULL,
    locked_at  TIMESTAMPTZ  NOT NULL,
    locked_by  VARCHAR(255) NOT NULL
);
COMMENT ON TABLE shedlock IS '분산 스케줄러 락(ShedLock) — 다중 인스턴스에서 자동청구를 1노드만 실행.';

